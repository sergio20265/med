// ─── Генерация блока промпта ─────────────────────────────────────────────────

function getUserPromptSection() {
  const lines = USERS.map((u) => {
    const id = u.username ? `@${u.username}` : u.phone;
    const topicsStr = u.topics.length
      ? ` Интересы: ${u.topics.join(", ")}.`
      : "";
    const extrasStr = u.extras ? ` ${u.extras}` : "";
    const langStr = u.language === "en" ? " Общение только на английском языке." : "";
    return `${id} — ${u.name} (${u.relation}). ${u.tone}${topicsStr}${extrasStr}${langStr}`;
  });

  return "ЛЮДИ В ЧАТЕ И ОТНОШЕНИЯ С НИМИ:\n\n" + lines.join("\n\n");
}

// ─── Поиск пользователя по Telegram-объекту ──────────────────────────────────

function findUser(telegramUser) {
  if (!telegramUser?.username) return null;
  const uname = telegramUser.username.toLowerCase();
  return USERS.find((u) => u.username?.toLowerCase() === uname) || null;
}

module.exports = { USERS, getUserPromptSection, findUser };
