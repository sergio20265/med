    log_error "–°–±–æ—Ä–∫–∞ –Ω–µ —É–¥–∞–ª–∞—Å—å. –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è dist/stacionar-site –Ω–µ –Ω–∞–π–¥–µ–Ω–∞."
    exit 1
